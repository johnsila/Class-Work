a="hello world"
#print entire characters string
print("1.",a[:])

#print first 6 characters of the string
print("2.",a[:6])

#count the first character of the string
print("3.",a[0])

#print the 3rd and 4th characters of the string
print("4.",a[2:5])

#print last character of the string
print("5.",a[-1])

#print entire string
print("6.",a[0:])

#white space is counted as a character
print("7.",a[5])

#print the 3rd and 4th characters of the string
print("8.",a[6:10])